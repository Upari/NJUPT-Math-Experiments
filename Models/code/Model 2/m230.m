m = 1235;

% 组一：对应 5000 次迭代 (选表格第 4 行: a=m/1000, b=m/1000, c=0.5)
Martin(1.235, 1.235, 0.5, 5000, 1);

% 组二：对应 10000 次迭代 (选表格第 6 行: a=m/100, b=m/10, c=-10)
Martin(12.35, 123.5, -10, 10000, 2);

% 组三：对应 15000 次迭代 (选表格第 7 行: a=-m/10, b=17, c=4)
Martin(-123.5, 17, 4, 15000, 3);

% 组四：对应 20000 次迭代 (选表格第 1 行: a=m, b=m, c=m)
Martin(1235, 1235, 1235, 20000, 4);


% ===================================================
% 2. 子函数定义部分（必须放在文件最末尾）
% ===================================================
function Martin(a, b, c, N, w)
    % a, b, c 为参数，N 为迭代次数，fig_num 为窗口编号
    x = zeros(1, N);
    y = zeros(1, N);
    x(1) = 0; y(1) = 0; % 初始点设为 (0,0)
    
    % 马丁迭代核心公式循环
    for n = 1:(N-1)
        x(n+1) = y(n) - sign(x(n)) * sqrt(abs(b * x(n) - c));
        y(n+1) = a - x(n);
    end
    
    % 开辟独立窗口画出离散点图
    figure(w);
    plot(x, y, '.', 'MarkerSize', 1); % 用极小的点展现混沌结构
    axis equal; % 让 XY 轴刻度等比例，图像不失真
    
    % 【已修改】直接在 title 中使用 num2str 转换，删除了原先的辅助函数
    title(['N=', num2str(N), ' (a=', num2str(a), ', b=', num2str(b), ', c=', num2str(c), ')']);
end