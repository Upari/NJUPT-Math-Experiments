% 人口数据
t = 1800:10:1980;
x = [5.3 7.2 9.6 12.9 17.1 23.2 31.4 38.6 50.2 ...
     62.9 76.0 92.0 106.5 123.2 131.7 150.7 179.3 204.0 226.5];

% 已知数据（w=5，对应1900年）
t1 = 1790;
x1 = 3.9;

t2 = 1900;
x2 = 76.0;

% 求x0和k
k = log(x2/x1)/(t2-t1);
x0 = x1/exp(k*t1);

% 预测人口
y = x0*exp(k*t);

% 误差
dif = y - x;

% 输出参数
disp('x0=')
disp(x0)

disp('k=')
disp(k)

% 人口预测值与误差一起输出
disp('预测人口 与 误差')

[y' dif']

% 作图
plot(t,x,'o',t,y,'-','LineWidth',1.5)

legend('实际','预测')
xlabel('年')
ylabel('人口(百万)')
grid on