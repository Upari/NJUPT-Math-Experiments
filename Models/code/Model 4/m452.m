% 人口数据
t = 1790:10:1980;
x = [3.9 5.3 7.2 9.6 12.9 17.1 23.2 31.4 38.6 50.2 ...
     62.9 76.0 92.0 106.5 123.2 131.7 150.7 179.3 204.0 226.5];

% 最小二乘拟合
p = polyfit(t,log(x),1);

% 求参数
k = p(1);
x0 = exp(p(2));

% 模型预测
y = x0*exp(k*t);

% 误差
dif = y - x;

% 输出参数
disp('x0=');disp(x0)

disp('k=');disp(k)

% 输出预测值和误差
disp('预测人口 与 误差')

[y' dif']

% 作图
plot(t,x,'o',t,y,'-','LineWidth',1.5)

legend('实际','拟合')
xlabel('年')
ylabel('人口(百万)')
grid on