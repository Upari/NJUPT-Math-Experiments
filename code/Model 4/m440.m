% 实验 4.4：最小二乘法求近似一次函数
clc; clear;
x = [1.5, 1.8, 2.4, 2.8, 3.4, 3.7, 4.2, 4.7, 5.3];
y = [8.9, 10.1, 12.4, 14.3, 16.2, 17.8, 19.6, 22.0, 24.1];

% 1. 构造并求解法方程组 A * a = B
A = [length(x), sum(x); sum(x), sum(x.^2)];
B = [sum(y); sum(x.*y)];
a = A \ B; % a(1)即为a0, a(2)即为a1

% 2. 计算误差平方和
e = sum((y - (a(1) + a(2)*x)).^2);

% 3. 输出结果
fprintf('a0 = %.4f, a1 = %.4f\n', a(1), a(2));
fprintf('函数方程: y = %.4f + %.4f*x\n', a(1), a(2));
disp(e);