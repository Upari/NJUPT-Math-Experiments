t = 1:27;
y = [21 65 127 281 558 923 1321 1801 2420 ...
     3125 3886 4638 5306 6028 6916 7646 8353 9049 ...
     9593 10098 10540 10910 11287 11598 11865 12086 12251];

% 1. 模型拟合（加分号隐去中间提示）
f1 = @(a, t) a(1) ./ (1 + a(2) * exp(-a(3) * t));
A1 = lsqcurvefit(f1, [13000, 50, 0.2], t, y);

f2 = @(a, t) a(1) ./ (1 + a(2) * exp(-a(3) * t - a(4) * t.^2));
A2 = lsqcurvefit(f2, [13000, 50, 0.2, 0.001], t, y);

% 2. 计算疫情结束时间
end1 = log(99 * A1(2)) / A1(3);
end2 = (-A2(3) + sqrt(A2(3)^2 + 4 * A2(4) * log(99 * A2(2)))) / (2 * A2(4));

% 3. 【直击题目问题】格式化输出最终答案
fprintf('  模型4.6函数：x(t) = %.1f / (1 + %.2f * exp(-%.4f * t))\n', A1(1), A1(2), A1(3));
fprintf('  模型4.7函数：x(t) = %.1f / (1 + %.2f * exp(-%.4f*t - %.4f*t^2))\n\n', A2(1), A2(2), A2(3), A2(4));

fprintf('  模型4.6预测：在1月20日后第 %.1f 天结束\n', end1);
fprintf('  模型4.7预测：在1月20日后第 %.1f 天结束\n', end2);
fprintf('===========================================================\n');

% 4. 【问题三要求】将散点和函数画在一张图上观察预测效果
plot(t, y, 'ko', t, f1(A1, t), 'r-', t, f2(A2, t), 'b--');
legend('实际数据', '模型4.6', '模型4.7'); grid on;